"R has 5 basic atomic classes of objects
 character, numeric, interger, logical (True/False)
- The most basic type of object is Vector.
- A vector can only contain objects of the same class.
- BUT, one exception is a list, it's represented as a vector. 
But each element can be of different class, say one element is a character, other is an integer etc.	

-- Numbers
1. Numbers in R are generally treated as numeric objects.
2. If you see a number like "1" or "2" in R, which you might think of as Integers, they are likely represented behind the
scenes as numeric objects(so something like "1.00" or "2.00")

If one needs an integer, we need to explicitly specify the L suffix.

